'use client';
import { useEffect } from "react";
import { ModelLayerInterface } from "@/interfaces";

const ModelLayer = ({
    isOpen,
    onClose,
    children,
    className,
    maxWidth = "max-w-xl",
}: ModelLayerInterface) => {
    useEffect(() => {
        const handleEscape = (event: KeyboardEvent) => {
            if (event.key === "Escape") {
                onClose();
            }
        };

        if (isOpen) {
            document.addEventListener("keydown", handleEscape);
            document.body.style.overflow = "hidden";
        }

        return () => {
            document.removeEventListener("keydown", handleEscape);
            document.body.style.overflow = "auto";
        };
    }, [isOpen, onClose]);

    if (!isOpen) return null;

    return (
        <div className={` ${className} fixed inset-0 flex items-center justify-center z-999999 h-full`}>
            <div
                className="absolute -top-[100%] inset-0 bg-black/50 backdrop-blur-sm"
                onClick={onClose}
            />
            <div className={`relative -top-[4%] bg-white dark:bg-gray-800 rounded-lg shadow-lg p-6 w-full mx-4 ${maxWidth} max-h-[95vh] overflow-y-auto`}>
                <button
                    onClick={onClose}
                    className="absolute top-2 right-2 text-gray-500 hover:text-gray-700 dark:text-gray-400 dark:hover:text-gray-200"
                >
                    âœ•
                </button>
                {children}
            </div>
        </div>
    );
}

export default ModelLayer;